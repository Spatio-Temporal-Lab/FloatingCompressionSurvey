#include <cmath>
namespace cengine {
u32 Utils::getBitsNeeded(u32 input)
{
   return std::max(std::floor(std::log2(input)) + 1, 1.0);
}
// -------------------------------------------------------------------------------------
u32 Utils::getBitsNeeded(u64 input)
{
   return std::max(std::floor(std::log2(input)) + 1, 1.0);
}
// -------------------------------------------------------------------------------------
u32 Utils::getBitsNeeded(s32 input)
{
    if (input < 0) {
        return 32;
    }
    return std::max(std::floor(std::log2(input)) + 1, 1.0);
}
}